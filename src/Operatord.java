public class Operatord {
    public static void main(String[] args) {
        int g=100;
        int i=20;
        System.out.println(g+i);
        System.out.println(g-i);
        System.out.println(g/i);
        System.out.println(g*i);
        System.out.println(g%i);
        //System.out.println(g++);
        System.out.println(++g);
    }
}
