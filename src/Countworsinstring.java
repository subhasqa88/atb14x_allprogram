public class Countworsinstring {
    public static void main(String[] args) {
        String b="subhas";
        int count=0;
        int length=b.length();
        System.out.println(length);
        for (int i=0;i<length;i++)
        {
            //count=count+1;
            count+=1;
        }
        System.out.println(count);
    }
}
